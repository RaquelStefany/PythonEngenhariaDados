# variáveis
idade = 21
nome = 'Raquel Rodrigues'
print(f'Meu nome é {nome} e eu tenho {idade} ano(s) de idade.')

idade, nome = (21, 'Raquel Rodrigues')
print(f'Meu nome é {nome} e eu tenho {idade} ano(s) de idade.')

