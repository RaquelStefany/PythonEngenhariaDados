# São operadores utilizados para verificar se um objeto está presente em uma sequência.

# Exemplo:
# Operador "in": Retorna True se o objeto está presente na sequência.
# Operador "not in": Retorna True se o objeto não está presente na sequência.

curso = "Curso de Python"
frutas = ["laranja", "uva", "limão"]
saques = [1500, 100]

# Operador "in"
print("Python" in curso)
print(200 in saques)

# Operador "not in"
print("maça" not in frutas)