# Os operadores aritméticos executam operações matemáticas, como adição, subtração com operandos.

# adição
print(2 + 2)

# subtração
print(2 - 2)

# multiplicação
print(2 * 2)

# divisão
print(2 / 2)

# divisão inteira
print(2 // 2)

# resto da divisão (modulo)
print(2 % 2)

# exponenciação
print(2 ** 2)