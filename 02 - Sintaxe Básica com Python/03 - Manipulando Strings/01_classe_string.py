curso = 'pYthon'

# Método capitalize() / title() - Retorna a string com a primeira letra maiúscula
print(curso.capitalize())

# Método upper() - Retorna a string com todas as letras maiúsculas
print(curso.upper())

# Método lower() - Retorna a string com todas as letras minúsculas
print(curso.lower())

# Método swapcase() - Retorna a string com as letras maiúsculas minúsculas e vice-versa
print(curso.swapcase())

curso = '          Python '

# Método strip() - Retorna a string sem os espaços em volta dela
print(curso.strip())

# Método lstrip() - Retorna a string sem os espaços à esquerda dela
print(curso.lstrip())

# Método rstrip() - Retorna a string sem os espaços à direita dela
print(curso.rstrip())

curso = 'Python'

# Método center() - Retorna a string centralizada na string de tamanho especificado
print(curso.center(10, '#'))

# Método join() - Retorna a string item entre ela
print('.'.join(curso))