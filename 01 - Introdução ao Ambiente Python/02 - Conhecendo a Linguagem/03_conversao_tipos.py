# int para float
preco = 10
preco = float(preco)
print(preco)

# float para int
preco = 10.5
preco = int(preco)
print(preco)

# int para string
idade = 21
idade = str(idade)
print(f'Fulano tem {idade} anos')

# string para int
idade = '21'
idade = int(idade)
print(f'Fulano tem {idade} anos')

# divisão
a = 10
b = 3
divisao = a / b
print(divisao)

# divisão inteira
a = 10
b = 3
divisao = a // b
print(divisao)