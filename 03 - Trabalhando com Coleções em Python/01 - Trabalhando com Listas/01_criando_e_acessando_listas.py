# Criação de lista usando [] (Colchetes)
## Lista com conteúdo definido
frutas_definida = ["laranja", "maça", "uva"]
print(frutas_definida)

## Lista vazia
frutas_vazia = []
print(frutas_vazia)


# Criação de lista usando list
## Lista de letras
letras = list("Python")
print(letras)

## Lista de números
numeros = list(range(10))
print(numeros)

## Lista de características de um carro
carro = ["Ferrari", "F8", 420000, 2020, 2900, "São Paulo", True]
print(carro)


# Retornar elementos de uma lista
## Retornar o primeiro elemento da lista
print(frutas_definida[0])

## Retornar ultimo elemento da lista
print(frutas_definida[-1])