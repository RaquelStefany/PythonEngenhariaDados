lista = ["p", "y", "t", "h", "o", "n"]

# Retornar a partir da 3ª posição
print(lista[2:])

# Retornar duas primeiras posições
print(lista[:2])

# Retornar da 2ª a 3ª posição
print(lista[1:3])

# Retornar por etapas
print(lista[0:3:2]) # Retorna da 1ª posição até a 3ª, pulando de 2 em 2

# Retornar invertido
print(lista[::-1]) # Inverte a lista